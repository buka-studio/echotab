"use client";

import { XIcon } from "@phosphor-icons/react";
import { Dialog as DialogPrimitive } from "radix-ui";
import * as React from "react";

import { Separator } from "./Separator";
import { cn } from "./util";

const Dialog: React.FC<DialogPrimitive.DialogProps & { scrollLock?: boolean }> = ({
  onOpenChange,
  scrollLock = true,
  ...props
}) => (
  <DialogPrimitive.Root
    onOpenChange={(open) => {
      if (!scrollLock) {
        if (open) {
          const timer = setTimeout(() => {
            document.body.style.pointerEvents = "";
          }, 0);

          return () => clearTimeout(timer);
        } else {
          document.body.style.pointerEvents = "auto";
        }
      }
      onOpenChange?.(open);
    }}
    {...props}
  />
);

function DialogTrigger({ ...props }: React.ComponentProps<typeof DialogPrimitive.Trigger>) {
  return <DialogPrimitive.Trigger data-slot="dialog-trigger" {...props} />;
}

function DialogPortal({ ...props }: React.ComponentProps<typeof DialogPrimitive.Portal>) {
  return <DialogPrimitive.Portal data-slot="dialog-portal" {...props} />;
}

function DialogClose({ ...props }: React.ComponentProps<typeof DialogPrimitive.Close>) {
  return <DialogPrimitive.Close data-slot="dialog-close" {...props} />;
}

function DialogOverlay({
  className,
  ...props
}: React.ComponentProps<typeof DialogPrimitive.Overlay>) {
  return (
    <DialogPrimitive.Overlay
      data-slot="dialog-overlay"
      className={cn(
        "data-[state=open]:animate-in data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=open]:fade-in-0 fixed inset-0 z-50 bg-black/50 backdrop-blur-xs dark:bg-black/60",
        className,
      )}
      {...props}
    />
  );
}

function DialogContent({
  className,
  children,
  overlay = true,
  close = true,
  container,
  ...props
}: React.ComponentProps<typeof DialogPrimitive.Content> & {
  close?: boolean;
  overlay?: boolean | React.ReactNode;
  container?: React.ComponentProps<typeof DialogPrimitive.Portal>["container"];
}) {
  return (
    <DialogPortal container={container} data-slot="dialog-portal">
      {overlay ? typeof overlay === "boolean" ? <DialogOverlay /> : overlay : null}

      <DialogPrimitive.Content
        data-slot="dialog-content"
        className={cn(
          "bg-popover scrollbar-gray data-[state=open]:animate-in data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=open]:fade-in-0 data-[state=closed]:zoom-out-95 data-[state=open]:zoom-in-95 fixed top-[50%] left-[50%] z-50 grid w-full max-w-[calc(100%-2rem)] translate-x-[-50%] translate-y-[-50%] gap-4 rounded-lg border p-4 shadow-lg duration-200 outline-none md:max-w-2xl",
          className,
        )}
        {...props}>
        {children}
        {close && (
          <DialogPrimitive.Close
            data-slot="dialog-close"
            className="ring-offset-background focus:ring-ring data-[state=open]:bg-accent data-[state=open]:text-muted-foreground absolute top-4 right-4 rounded-xs opacity-70 transition-opacity hover:opacity-100 focus:ring-2 focus:ring-offset-2 focus:outline-hidden disabled:pointer-events-none [&_svg]:pointer-events-none [&_svg]:shrink-0 [&_svg:not([class*='size-'])]:size-4">
            <XIcon className="h-4 w-4" />
            <span className="sr-only">Close</span>
          </DialogPrimitive.Close>
        )}
      </DialogPrimitive.Content>
    </DialogPortal>
  );
}

function DialogHeader({ className, ...props }: React.ComponentProps<"div">) {
  return (
    <>
      <div
        data-slot="dialog-header"
        className={cn("flex flex-col gap-1 text-center sm:text-left", className)}
        {...props}
      />
      <Separator className="-mx-4 w-[calc(100%+2rem)]! has-[+_[data-slot=footer-separator]]:hidden" />
    </>
  );
}

function DialogFooter({ className, ...props }: React.ComponentProps<"div">) {
  return (
    <>
      <Separator data-slot="footer-separator" className="-mx-4 w-[calc(100%+2rem)]!" />
      <div
        data-slot="dialog-footer"
        className={cn("flex flex-col-reverse gap-1 sm:flex-row sm:justify-end", className)}
        {...props}
      />
    </>
  );
}

function DialogTitle({ className, ...props }: React.ComponentProps<typeof DialogPrimitive.Title>) {
  return (
    <DialogPrimitive.Title
      data-slot="dialog-title"
      className={cn("text-lg leading-none font-semibold", className)}
      {...props}
    />
  );
}

function DialogDescription({
  className,
  ...props
}: React.ComponentProps<typeof DialogPrimitive.Description>) {
  return (
    <DialogPrimitive.Description
      data-slot="dialog-description"
      className={cn("text-muted-foreground text-sm", className)}
      {...props}
    />
  );
}

export {
  Dialog,
  DialogClose,
  DialogContent,
  DialogDescription,
  DialogFooter,
  DialogHeader,
  DialogOverlay,
  DialogPortal,
  DialogTitle,
  DialogTrigger,
};
